            <h1>Présentation Brevet de technicien supérieur (BTS) - Services informatiques aux organisations (SIO) </h1>
            <p>
            <h4>Ce BTS permet la formation à trois domaines d'activité en 2 ans:</h4>
            -Support et mise à disposition de services informatiques : assistance technique et gestion des infrastructures<br>
            -Cybersécurité des services informatiques : sécurisation des données et infrastructures<br>
            -Conception et développement d'applications : création de solutions logicielles adaptées aux besoins des utilisateurs</p>
        </section>
        <section>
            <img src="../images/siocours.png" width="90%">
        </section>
        <section>
            <h1> Il existe deux options : </h1>
            <p>-SISR (Solutions d’Infrastructure, Systèmes et Réseaux) : elle forme les étudiants à concevoir, installer, administrer et sécuriser des infrastructures réseaux et systèmes informatiques. Elle est idéale pour ceux qui souhaitent travailler dans la gestion de serveurs, le cloud, la cybersécurité ou les réseaux.</p>
            <p>-SLAM (Solutions Logicielles et Applications Métiers), celle que j'ai choisie : elle se concentre sur le développement, la programmation et la gestion d'applications informatiques. Elle prépare les étudiants à concevoir, coder, tester et maintenir des logiciels adaptés aux besoins des entreprises. </p>
        </section>
        <section>
            <h1>Les débouchés</h1>
            <h4>Débouchés de l'option SISR (Solutions d’Infrastructure, Systèmes et Réseaux) :</h4>
            Administrateur systèmes et réseaux<br>
            Technicien réseau<br>
            Technicien support informatique<br>
            Responsable infrastructure IT<br>
            Consultant en cybersécurité<br>
            Ingénieur systèmes (après expérience ou études supérieures)<br>

            <h4>Débouchés de l'option SLAM (Solutions Logicielles et Applications Métiers) :</h4>
            Développeur d'applications<br>
            Développeur web ou mobile<br>
            Technicien en développement informatique<br>
            Analyste programmeur<br>
            Testeur/Intégrateur d’applications<br>
            Chef de projet en développement (après expérience ou études supérieures)<br>
        </section>
