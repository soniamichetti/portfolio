            <h1>1ère veille : L’évolution des stratégies de sécurité des mots de passe </h1>
            <p>Dans le monde de la sécurité informatique, la question des mots de passe est un enjeu majeur du côté des informaticiens. Ces derniers sont chargés de concevoir des stratégies pour renforcer la protection des données contre des menaces potentielles. On verra quelles sont les solutions que les informaticiens trouvent contre les défis et les approches dont ils font face pour assurer une gestion efficace des mots de passe. </p>
            <p> Stockage en clair (obsolète) : Les mots de passe étaient initialement stockés sans aucune protection, ce qui les rendait accessibles en cas de fuite de données. Cette méthode est aujourd’hui interdite.
            </p>
            <!-- <img src="../images/image-2.png"> -->
            <p>Chiffrement (réversible) : Les mots de passe ont ensuite été chiffrés, mais comme cette méthode est réversible, elle ne respecte pas les règles actuelles de sécurité.
            </p>
            <!-- <img src="../images/image-3.png"> -->
            <p>Hachage (irréversible) : Les mots de passe sont convertis en chaînes uniques grâce à des algorithmes comme MD5 ou SHA1. Cependant, ces fonctions sont devenues obsolètes face à des attaques comme les "rainbow tables".
            </p>
            <!-- <img src="../images/image-4.png"> -->
            <!-- <p>Ajout de sel et de poivre :</p> -->
            <p>Sel : Une chaîne unique ajoutée au mot de passe avant son hachage, différente pour chaque utilisateur.
                <br>Poivre : Une chaîne commune à tous les mots de passe, conservée en dehors de la base de données pour plus de sécurité.
            </p>
            <!-- <div class="img-veille">
                <img src="../images/image-5_1.jpg">
                <img src="../images/image-5_2.jpg">
            </div>-->

            <p>Itérations de hachage : Les mots de passe sont hachés plusieurs fois pour ralentir les attaques par force brute.
            </p>
            <!-- <div class="img-veille">
                <img src="../images/image-6_1.jpg">
                <img src="../images/image-6_2.jpg">
            </div> -->
            
            <p>Benchmarking : Les algorithmes de hachage sont évalués pour garantir leur résistance face aux nouvelles menaces.
            </p>
            <p>
            <h4>Conclusion</h4>
            L'évolution des stratégies de sécurité des mots de passe reflète une course sans fin entre protection et attaque. Les progrès technologiques doivent être continuellement intégrés pour rester un pas en avance sur les cybercriminels.</p>
